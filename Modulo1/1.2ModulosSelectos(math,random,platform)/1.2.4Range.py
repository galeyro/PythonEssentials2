from random import randrange, randint

print(randrange(10), end=' ') #un numero entre 0 y 9
print(randrange(5, 10), end=' ') #un numero entre 5 y 9
print(randrange(0, 10, 2), end=' ')#un numero entre 0 y 9, pero solo pares
print(randint(0, 1)) #un numero entre 0 y 1 incluyendo 0 y 1
print(randint(0,10))#un numero entre 0 y 10 incluyendo 0 y 10